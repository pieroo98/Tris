/*----------------------------------------------------------------------------
  A/D IRQ: Executed when A/D Conversion is ready (signal from ADC peripheral)
 *----------------------------------------------------------------------------*/
#include "adc.h"

unsigned short AD_current;

void ADC_IRQHandler(void) {	
	AD_current = ((LPC_ADC->ADGDR>>4) & 0xFFF);
	AD_current = AD_current*240/0xfff;
}
